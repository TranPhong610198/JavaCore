public class Instanceof_Operator {
    public static void main(String[] args){
        String name = new String();
        System.out.println(name instanceof String); //print true
        
        Instanceof_Operator Blabla = new Instanceof_Operator();
        System.out.println(Blabla instanceof Instanceof_Operator); //print true;
        
        // It's also work with interface and Object class
        
    }
}
